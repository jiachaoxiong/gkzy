function getMajors(ctype) {
  return (MAJOR_POOLS[ctype] || MAJOR_POOLS['综合']).slice(0, 6);
}

function getScoreParams(level, ctype) {
  if (level === '985') return ['本科一批A', 620, 600, 3000];
  if (level === '211') return ['本科一批A', 555, 545, 15000];
  if (level === '双一流') return ['本科一批A', 535, 525, 22000];
  const r = hash(ctype + level) % 100;
  if (r < 30) return ['本科一批B', 505, 500, 35000];
  if (r < 70) return ['本科二批A', 450, 445, 65000];
  return ['本科二批B', 400, 400, 90000];
}

