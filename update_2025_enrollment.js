async function update2025() {
  const conn = await mysql.createConnection({
    host: 'localhost', user: 'root', password: '123456',
    charset: 'utf8mb4', database: 'gkzy'
  });
  console.log('✅ 数据库连接成功\n');

  const [cols] = await conn.query('SELECT id, name FROM college');
  const map = {};
  cols.forEach(c => { map[c.name] = c.id; });

  let n = 0, miss = 0;

  for (const [name, [sci, arts]] of Object.entries(DATA_2025)) {
    const cid = map[name];
    if (!cid) { miss++; console.log('  ⚠️ 未找到: '+name); continue; }

    if (sci > 0) {
      const [r] = await conn.query('UPDATE admission_score SET enroll_count = ' + sci + ' WHERE college_id = ' + cid + ' AND year = 2025 AND subject_type = \"理工\"');
      n += r.affectedRows;
    }
    if (arts > 0) {
      const [r] = await conn.query('UPDATE admission_score SET enroll_count = ' + arts + ' WHERE college_id = ' + cid + ' AND year = 2025 AND subject_type = \"文史\"');
      n += r.affectedRows;
    }
    console.log('  ✅ '+name.padEnd(18)+' 物理'+sci+' / 历史'+arts);
  }

  // 统计
  const [st] = await conn.query('SELECT COUNT(*) total, SUM(CASE WHEN enroll_count IS NOT NULL THEN 1 ELSE 0 END) has_2025 FROM admission_score WHERE year=2025');
  console.log('\n📊 2025年数据统计: '+st[0].has_2025+'/'+st[0].total+' 有真实录取人数');
  console.log('本轮更新: '+n+' 条 / 未匹配: '+miss+' 所');

  // 总览
  const [all] = await conn.query('SELECT COUNT(*) total, SUM(CASE WHEN enroll_count IS NOT NULL THEN 1 ELSE 0 END) has_real FROM admission_score');
  console.log('\n📊 全库总览: '+all[0].has_real+'/'+all[0].total+' 有真实录取人数 ('+(all[0].has_real/all[0].total*100).toFixed(1)+'%)');

  // 抽查
  console.log('\n=== 2025数据抽查 ===');
  for (const name of ['北京大学','清华大学','太原理工大学','山西大学','山东大学','哈尔滨工业大学']) {
    const cid = map[name];
    if (!cid) continue;
    const [r] = await conn.query('SELECT year, subject_type, enroll_count, batch FROM admission_score WHERE college_id='+cid+' AND year=2025 ORDER BY subject_type');
    console.log(name+': '+r.map(x=>x.subject_type+'='+(x.enroll_count!=null?x.enroll_count:'NULL')).join(', '));
  }

  await conn.end();
  console.log('\n✅ 2025年录取人数更新完成!');
}

update2025().catch(err => { console.error('❌', err.message); process.exit(1); });
